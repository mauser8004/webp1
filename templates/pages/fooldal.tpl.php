    <section class="advantages">
        <h2>Előnyök</h2>
        <ul>
            <li><strong>Könnyen tanulható:</strong> Egyszerű programozási nyelv (C++ alapú)</li>
            <li><strong>Olcsó:</strong> Kezdő csomagok már 10.000 Ft-tól</li>
            <li><strong>Bővíthető:</strong> Shield-ekkel (kiegészítő táblák) funkcionalitás növelhető</li>
            <li><strong>Nagy közösség:</strong> Több ezer példa projekt és tutorial</li>
        </ul>
    </section>
    
